When you see labels like AA, AB, or PB on Kenyan coffee, you''re looking at a grading system that has been refined over decades to ensure consistent quality standards.

### Size Matters

**AA Grade**
The largest beans, retained on a screen size 17-18 (6.8mm). These beans are prized for their uniform roasting and often command premium prices.

**AB Grade**
A combination of A (screen 16) and B (screen 15) grades. While slightly smaller, AB coffees often deliver exceptional cup quality and represent excellent value.

**PB (Peaberry)**
A natural mutation where only one bean develops inside the cherry instead of two. Peaberries are rounded and often have concentrated, intense flavors.

